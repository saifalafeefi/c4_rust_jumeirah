// Simple test for comparison operators in if statements

int main() {
    int a = 10;
    int b = 5;
    
    // Test greater than
    if (a > b) {
        printf("a is greater than b\n");
    } else {
        printf("a is not greater than b\n");
    }
    
    // Test less than
    if (a < b) {
        printf("a is less than b\n");
    } else {
        printf("a is not less than b\n");
    }
    
    return 0;
} 