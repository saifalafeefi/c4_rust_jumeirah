int main() {
    // Single variable
    int single;
    single = 0;
    printf("Single: %d\n", single);
    
    // Multiple variables
    int first;
    int second;
    int third;
    int fourth;
    first = 1;
    second = 2;
    third = 3;
    fourth = 4;
    printf("First: %d\n", first);
    printf("Second: %d\n", second);
    printf("Third: %d\n", third);
    printf("FOURTH: %d\n", fourth);
    
    return 0;
} 